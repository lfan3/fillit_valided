#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			index;
	unsigned char	*str;

	index = -1;
	str = (unsigned char *)s;
	while (++index < n)
		if (str[index] == (unsigned char)c)
			return (&(str[index]));
	return (NULL);
}
