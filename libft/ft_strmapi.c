#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		len;
	int		i;

	if (s == NULL || f == NULL)
		return (NULL);
	len = ft_strlen((char*)s);
	str = ft_strnew(len);
	i = -1;
	if (str)
		while (++i < len)
			str[i] = f(i, s[i]);
	return (str);
}
