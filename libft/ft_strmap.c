#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		len;
	int		i;

	if (s == NULL || f == NULL)
		return (NULL);
	len = ft_strlen((char*)s);
	str = ft_strnew(len);
	i = -1;
	if (str)
		while (++i < len)
			str[i] = f(s[i]);
	return (str);
}
