#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len1;
	int		len2;

	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	if (!(str = ft_strnew(ft_strlen((char*)s1) + ft_strlen((char*)s2))))
		return (NULL);
	len1 = -1;
	while (s1[++len1])
		str[len1] = s1[len1];
	len2 = -1;
	while (s2[++len2])
		str[len1++] = s2[len2];
	str[len1] = '\0';
	return (str);
}
