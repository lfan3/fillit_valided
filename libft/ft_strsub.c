#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*str;

	if (!s || !*s)
		return (NULL);
	if (!(str = ft_strnew(len)))
		return (NULL);
	i = -1;
	while (++i < len && s[start])
		str[i] = s[start + i];
	return (str);
}
