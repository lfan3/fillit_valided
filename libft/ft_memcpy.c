#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*psrc;
	unsigned char	*pdst;

	psrc = (unsigned char *)src;
	pdst = (unsigned char *)dst;
	if (dst == src)
		return (dst);
	while (n--)
		*pdst++ = *psrc++;
	return (dst);
}
